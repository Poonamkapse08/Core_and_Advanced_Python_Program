'1. Calculate the multiplication and sum of two numbers in simple python program'

# Input: Two numbers
num1 = float(input("Enter the first number: "))
num2 = float(input("Enter the second number: "))

# Multiply the numbers
multiplication = num1 * num2

# Sum the numbers
addition = num1 + num2

# Output the results
print(f"The multiplication of {num1} and {num2} is: {multiplication}")
print(f"The sum of {num1} and {num2} is: {addition}")

'''Output: Enter the first number: 23
Enter the second number: 33
The multiplication of 23.0 and 33.0 is: 759.0
The sum of 23.0 and 33.0 is: 56.0'''
